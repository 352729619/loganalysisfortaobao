package ControlPanelDataOp;

import java.io.Serializable;

public class CommInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String Pv;

	private String Uv;

	private String Title;

	private String Url;

	private String BounceRate;

	private String MouseInfoVolume;

	private String FromType;

	public String getPv() {
		return Pv;
	}

	public void setPv(String pv) {
		Pv = pv;
	}

	public String getUv() {
		return Uv;
	}

	public void setUv(String uv) {
		Uv = uv;
	}

	public String getTitle() {
		return Title;
	}

	public void setTitle(String title) {
		Title = title;
	}

	public String getUrl() {
		return Url;
	}

	public void setUrl(String url) {
		Url = url;
	}

	public String getBounceRate() {
		return BounceRate;
	}

	public void setBounceRate(String bounceRate) {
		BounceRate = bounceRate;
	}

	public String getMouseInfoVolume() {
		return MouseInfoVolume;
	}

	public void setMouseInfoVolume(String mouseInfoVolume) {
		MouseInfoVolume = mouseInfoVolume;
	}

	public String getFromType() {
		return FromType;
	}

	public void setFromType(String fromType) {
		FromType = fromType;
	}

}
